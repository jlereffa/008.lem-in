#include <lem_in.h>

int	check_path(char *s)
{
	if (!s || !*s || *s == '#' || *s == 'L' || *s < 32 || *s > 126 || *s == ' ')
		return (0);
	while (*s && *s >= 32 && *s <= 126 && *s != '-' && *s != ' ')
		s++;
	if (!*s || *s != '-')
		return (0);
	s++;
	if (!*s || *s < 32 || *s > 126 || *s == ' ')
		return (0);
	while (*s && *s >= 32 && *s <= 126 && *s != ' ')
		s++;
	if (*s)
		return (0);
	return (1);
}

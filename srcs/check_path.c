#include <lem_in.h>

int	check_path(char *s)
{
	if (!s || !*s || !(*s >= '0' && *s <= '9'))
		return (0);
	while (*s && *s >= '0' && *s <= '9')
		s++;
	if (!*s || *s != '-')
		return (0);
	s++;
	if (!*s || !(*s >= '0' && *s <= '9'))
		return (0);
	while (*s && *s >= '0' && *s <= '9')
		s++;
	if (*s)
		return (0);
	return (1);
}

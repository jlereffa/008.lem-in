#include <lem_in.h>

void	putf(char *s, char *n)
{
	ft_putstr_fd("\033[1;35m", 2);
	ft_putstr_fd(s, 2);
	ft_putstr_fd(n, 2);
	ft_putstr_fd("\033[0m", 2);
}
